package model;

public class beverage {

    private String name;
    private String price;

    public String getName() {
        return this.name;
    }

    public void setName(String newName) {
        this.name = newName;
    }

    public String getPrice() {
        return this.price;
    }

    public void setPrice(String newPrice) {
        this.price = newPrice;
    }


    
}
